<?php

$i = 1;
echo "{$i} \n";

echo "\n今までに学習した普通の加算　\n";
$i = $i +1;
echo "{$i} \n";

echo "\n複合演算子を使って「プラス１」する \n";
$i += 1;
echo "{$i} \n";

echo "\n 加算子を使って「プラス1」する \n";
++ $i;
echo "{$i}\n";

echo "\n 減算子を使って「マイナス1」する \n";
-- $i;
echo "{$i}\n";

echo "\n 複合演算を使って「マイナス1」する\n";
$i -= 1;
echo "{$i}\n";